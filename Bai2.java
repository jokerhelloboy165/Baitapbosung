//Khai báo đúng là:
//
//e. public static void main(String [] a){/* Các câu lệnh */}
//
//Khai báo sai là:
//
//a. static void main(String arg) { /* Các câu lệnh */}
//
//Sai vì tham số đầu vào của hàm main phải là một mảng các chuỗi (String[] args), không phải là một chuỗi đơn (String arg).
//b. Public static Void main(String arg[]){/* Các câu lệnh */}
//
//Sai vì phải sử dụng "void" (không trả về giá trị) thay vì "Void" (không phải là kiểu dữ liệu hợp lệ).
//c. public static void main(String arg) { /* Các câu lệnh */}
//
//Sai vì tham số đầu vào của hàm main phải là một mảng các chuỗi (String[] args), không phải là một chuỗi đơn (String arg).
//d. public static int main(string ar[]){/* Các câu lệnh */}
//
//Sai vì hàm main phải trả về kiểu void (không trả về giá trị), không phải là kiểu int.
//Vì vậy, khai báo đúng là "public static void main(String [] a)" và tất cả các khai báo khác đều sai.
